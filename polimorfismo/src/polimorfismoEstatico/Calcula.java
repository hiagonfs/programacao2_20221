package polimorfismoEstatico;

public class Calcula {

	public Calcula() {
	}

	public int square(int a, int b) {
		return a * b;
	}

	public double square(double a, double b) {
		return a * b;
	}

	public double square(int a, double b) {
		return a * b;
	}

	public double square(double a, int b) {
		return a * b;
	}

	public float square(int a, float b) {
		return a * b;
	}

}
